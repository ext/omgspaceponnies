std::vector<vector_t> spawn_pts;

void init_level() {
	for(int x = 0; x < MAP_WIDTH; ++x) {
		for(int y = 0; y<MAP_HEIGHT; ++y) {
			if(map_value(x,y) == -1)
				spawn_pts.push_back(vector_t(x*64,y*64));
		}
	}
}

int map_value(int x, int y) {
	if(x < 0 || x >= MAP_WIDTH)
		return 0;
	if(y < 0 || y >= MAP_HEIGHT) 
		return 0;

	return map[y][x];
}

void calc_map_index(const vector_t &pos, int &x, int &y) {
	x = floor((pos.x+32)/64);
	y = floor((pos.y+32)/64);
}

vector_t get_rand_spawn() {
	int r = rand() % spawn_pts.size();
	return spawn_pts[r];
}
